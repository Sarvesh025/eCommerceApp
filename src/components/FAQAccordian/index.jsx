const SubAccItem = ({ subdata }) => {
    const [open, setOpen] = useState(false);
    return (
        <div>
            <div className={`${styles.accordion} ${open && styles.active}`}
                onClick={() => {
                    setOpen(!open);
                }}>{subdata.header}
                <div>
                    <h2>{open ? '-' : '+'} </h2>
                </div>
            </div>
            <div className={styles.panel} style={open ? { display: 'block' } : {}}>
                <p>{subdata.data}</p>
            </div>
        </div>
    )
}

const AccItem = ({ accordionUnitData }) => {
    const [open, setOpen] = useState(false);
    return (
        <div>
            <div className={`${styles.accordion} ${open && styles.active}`}
                onClick={() => {
                    setOpen(!open);
                }}>{accordionUnitData.header}
                <div>
                    <h2>{open ? '-' : '+'} </h2>
                </div>
            </div>
            <div className={styles.panel} style={open ? { display: 'block' } : {}}>
                {accordionUnitData.subdata.map((item, index) => {
                    return (
                        <div key={index}>
                            <SubAccItem subdata={item} />
                        </div>
                    )
                })}

            </div>
        </div>
    )
}


const Accordion = ({ accordionData = data }) => {


    return (
        <div>
            {
                accordionData.map((item, index) => {
                    return (
                        <div key={index}>
                            <AccItem accordionUnitData={item} />
                        </div>
                    )
                })
            }
        </div>
    )
};

export default Accordion;